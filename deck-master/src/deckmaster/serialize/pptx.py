"""Native OOXML serializer: a RenderedDeck becomes a .pptx package.

Everything here is emitted as strings and packed with `zipfile`. There is no
PPTX library involved, which means every schema obligation is ours to meet.
Three of them decide whether PowerPoint opens the file or offers to repair it,
and none of them produce a visible symptom in more permissive viewers:

1. **Child element order.** OOXML types are XSD *sequences*, not choices. A
   `<a:ln>` placed before `<a:solidFill>` inside `<p:spPr>` is a schema
   violation even though both elements are legal there.
2. **Identifier rules.** Every `cNvPr@id` must be unique within its slide and
   non-zero; every `sldId@id` must be at least 256.
3. **Package integrity.** Every part needs a content-type declaration, and every
   `r:id` needs a relationship that resolves.

XML is written by hand rather than through ElementTree because byte-identical
output depends on controlling attribute order and self-closing tags, which
ElementTree does not guarantee across versions.
"""

from __future__ import annotations

import zipfile
from dataclasses import dataclass

from ..scene import Align, Anchor, LineShape, RectShape, RenderedDeck, RenderedSlide, Shape, TextShape
from ..units import CANVAS_H_PT, CANVAS_W_PT, pt_to_emu

XML_DECL = '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>\n'

NS_P = "http://schemas.openxmlformats.org/presentationml/2006/main"
NS_A = "http://schemas.openxmlformats.org/drawingml/2006/main"
NS_R = "http://schemas.openxmlformats.org/officeDocument/2006/relationships"

#: A fixed instant for every timestamp in the package. Real build times would
#: make two builds of the same deck differ, which would defeat the determinism
#: guarantee for no benefit -- nobody consults a generated deck's created-date.
FROZEN_TIMESTAMP = "2020-01-01T00:00:00Z"

#: DOS epoch. zipfile cannot encode anything earlier.
FROZEN_ZIP_DATE = (1980, 1, 1, 0, 0, 0)


_INF = float("inf")

#: Characters XML 1.0 forbids outright. They cannot be escaped -- `&#x1F;` is
#: itself illegal -- so they are removed rather than encoded. Tab, newline and
#: carriage return are the only control characters the standard permits.
_ILLEGAL_XML_CHARS = {c for c in range(0x00, 0x20)} - {0x09, 0x0A, 0x0D}


def _finite(value: float, what: str) -> float:
    """Reject a value that cannot be written as an integer attribute.

    A NaN or infinity here is always a defect in arithmetic upstream, never bad
    content. Caught at emission it names the value; allowed through it becomes
    either an attribute PowerPoint refuses to parse or a conversion error
    identifying nothing.
    """
    if value != value or value in (_INF, -_INF):
        raise ValueError(
            f"cannot emit non-finite {what} {value!r}; "
            "this is a defect in layout arithmetic, not in the deck's content"
        )
    return value


def hundredths(value: float, what: str) -> int:
    """Emit a value the schema types in hundredths -- font size, line spacing."""
    return int(round(_finite(value, what)))


def emu(value: float) -> int:
    """Convert points to EMU for emission, refusing anything unrepresentable.

    Every number this module writes into an attribute goes through here. EMU
    attributes are integer-typed in the schema, so a non-finite value would
    either produce an attribute PowerPoint refuses to parse or fail deep inside
    conversion with a message naming neither the value nor the shape it came
    from. A NaN or infinity in layout arithmetic is always a bug upstream, and
    this is the last point that can still say so clearly.

    Guarding at the emission boundary rather than inside the conversion helper
    is deliberate: this module is the only thing that writes attributes, so one
    checkpoint here covers every path without constraining arithmetic elsewhere.
    """
    return pt_to_emu(_finite(value, "coordinate"))


def escape(text: str) -> str:
    """Escape text for use in an XML text node or a quoted attribute.

    Control characters are *stripped*, not escaped. XML 1.0 forbids them
    outright, so `&#x1F;` is itself illegal and encoding one produces a document
    no parser will accept. Since deck content routinely arrives pasted from
    elsewhere or generated by a model, a stray control byte is a realistic input
    rather than a theoretical one -- and silently dropping it is the only
    representable outcome.

    The ampersand must be replaced first, or the escapes introduced by the later
    replacements would themselves be escaped.
    """
    if any(ord(c) in _ILLEGAL_XML_CHARS for c in text):
        text = "".join(c for c in text if ord(c) not in _ILLEGAL_XML_CHARS)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def _emu_xfrm(x: float, y: float, w: float, h: float, flip_h: bool = False, flip_v: bool = False) -> str:
    flips = ""
    if flip_h:
        flips += ' flipH="1"'
    if flip_v:
        flips += ' flipV="1"'
    return (
        f"<a:xfrm{flips}>"
        f'<a:off x="{emu(x)}" y="{emu(y)}"/>'
        f'<a:ext cx="{emu(w)}" cy="{emu(h)}"/>'
        f"</a:xfrm>"
    )


