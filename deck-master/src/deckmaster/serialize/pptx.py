def _rect_xml(shape: RectShape, shape_id: int) -> str:
    r = shape.rect
    if shape.fill:
        fill = f'<a:solidFill><a:srgbClr val="{shape.fill}"/></a:solidFill>'
    else:
        fill = "<a:noFill/>"

    if shape.stroke:
        dash = '<a:prstDash val="dash"/>' if shape.stroke.dashed else '<a:prstDash val="solid"/>'
        line = (
            f'<a:ln w="{emu(shape.stroke.width)}">'
            f'<a:solidFill><a:srgbClr val="{shape.stroke.color}"/></a:solidFill>'
            f"{dash}"
            f"</a:ln>"
        )
    else:
        line = "<a:ln><a:noFill/></a:ln>"

    return (
        "<p:sp>"
        "<p:nvSpPr>"
        f'<p:cNvPr id="{shape_id}" name="{escape(shape.name)}"/>'
        "<p:cNvSpPr/>"
        "<p:nvPr/>"
        "</p:nvSpPr>"
        "<p:spPr>"
        f"{_emu_xfrm(r.x, r.y, r.w, r.h)}"
        '<a:prstGeom prst="rect"><a:avLst/></a:prstGeom>'
        f"{fill}{line}"
        "</p:spPr>"
        # A p:sp must carry a txBody even when it holds no text.
        '<p:txBody><a:bodyPr/><a:lstStyle/><a:p/></p:txBody>'
        "</p:sp>"
    )


def _text_xml(shape: TextShape, shape_id: int) -> str:
    r = shape.rect
    size = hundredths(shape.size_pt * 100, "font size")
    spacing = hundredths(shape.line_height * 100000, "line spacing")
    bold = ' b="1"' if shape.bold else ' b="0"'

    paragraphs = []
    for line in shape.lines:
        if line:
            run = (
                f'<a:r><a:rPr lang="en-US" sz="{size}"{bold} dirty="0">'
                f'<a:solidFill><a:srgbClr val="{shape.color}"/></a:solidFill>'
                f'<a:latin typeface="Arial"/><a:cs typeface="Arial"/>'
                f"</a:rPr><a:t>{escape(line)}</a:t></a:r>"
            )
        else:
            # An empty line still needs a run-properties carrier so the blank
            # paragraph keeps the right height.
            run = f'<a:endParaRPr lang="en-US" sz="{size}"{bold}/>'
        paragraphs.append(
            f'<a:p><a:pPr algn="{shape.align.value}">'
            f'<a:lnSpc><a:spcPct val="{spacing}"/></a:lnSpc>'
            f"</a:pPr>{run}</a:p>"
        )

    inset = emu(shape.inset)
    body_pr = (
        f'<a:bodyPr wrap="square" lIns="{inset}" tIns="{inset}" rIns="{inset}" bIns="{inset}" '
        f'anchor="{shape.anchor.value}">'
        # Autofit is disabled deliberately: the layout already decided the line
        # breaks and the box height. Letting PowerPoint rescale text would
        # silently invalidate every measurement the audit verified.
        "<a:noAutofit/>"
        "</a:bodyPr>"
    )

    return (
        "<p:sp>"
        "<p:nvSpPr>"
        f'<p:cNvPr id="{shape_id}" name="{escape(shape.name)}"/>'
        '<p:cNvSpPr txBox="1"/>'
        "<p:nvPr/>"
        "</p:nvSpPr>"
        "<p:spPr>"
        f"{_emu_xfrm(r.x, r.y, r.w, r.h)}"
        '<a:prstGeom prst="rect"><a:avLst/></a:prstGeom>'
        "<a:noFill/>"
        "</p:spPr>"
        f"<p:txBody>{body_pr}<a:lstStyle/>{''.join(paragraphs)}</p:txBody>"
        "</p:sp>"
    )


def _line_xml(shape: LineShape, shape_id: int) -> str:
    x, y = min(shape.x1, shape.x2), min(shape.y1, shape.y2)
    w, h = abs(shape.x2 - shape.x1), abs(shape.y2 - shape.y1)
    dash = '<a:prstDash val="dash"/>' if shape.stroke.dashed else '<a:prstDash val="solid"/>'

    ends = ""
    if shape.arrow_start:
        ends += '<a:headEnd type="triangle" w="med" len="med"/>'
    if shape.arrow_end:
        ends += '<a:tailEnd type="triangle" w="med" len="med"/>'

    return (
        "<p:sp>"
        "<p:nvSpPr>"
        f'<p:cNvPr id="{shape_id}" name="{escape(shape.name)}"/>'
        "<p:cNvSpPr/>"
        "<p:nvPr/>"
        "</p:nvSpPr>"
        "<p:spPr>"
        f"{_emu_xfrm(x, y, w, h, flip_h=shape.x2 < shape.x1, flip_v=shape.y2 < shape.y1)}"
        '<a:prstGeom prst="line"><a:avLst/></a:prstGeom>'
        "<a:noFill/>"
        f'<a:ln w="{emu(shape.stroke.width)}" cap="flat">'
        f'<a:solidFill><a:srgbClr val="{shape.stroke.color}"/></a:solidFill>'
        f"{dash}{ends}"
        "</a:ln>"
        "</p:spPr>"
        '<p:txBody><a:bodyPr/><a:lstStyle/><a:p/></p:txBody>'
        "</p:sp>"
    )


def _shape_xml(shape: Shape, shape_id: int) -> str:
    if isinstance(shape, RectShape):
        return _rect_xml(shape, shape_id)
    if isinstance(shape, TextShape):
        return _text_xml(shape, shape_id)
    if isinstance(shape, LineShape):
        return _line_xml(shape, shape_id)
    raise TypeError(f"cannot serialize {type(shape).__name__}")


