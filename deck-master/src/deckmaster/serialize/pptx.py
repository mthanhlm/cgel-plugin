def build_parts(deck: RenderedDeck) -> list[tuple[str, str]]:
    """Every part in the package, in a fixed order.

    Order is fixed rather than derived from a dict so that the ZIP's central
    directory is identical on every build and on every platform.
    """
    n = len(deck.slides)
    parts: list[tuple[str, str]] = [
        ("[Content_Types].xml", _content_types_xml(n)),
        ("_rels/.rels", _root_rels_xml()),
        ("docProps/app.xml", _app_xml(deck)),
        ("docProps/core.xml", _core_xml(deck)),
        ("ppt/presentation.xml", _presentation_xml(n)),
        ("ppt/_rels/presentation.xml.rels", _presentation_rels_xml(n)),
        ("ppt/slideMasters/slideMaster1.xml", _slide_master_xml()),
        (
            "ppt/slideMasters/_rels/slideMaster1.xml.rels",
            _simple_rels_xml(
                [
                    ("rId1", "slideLayout", "../slideLayouts/slideLayout1.xml"),
                    ("rId2", "theme", "../theme/theme1.xml"),
                ]
            ),
        ),
        ("ppt/slideLayouts/slideLayout1.xml", _slide_layout_xml()),
        (
            "ppt/slideLayouts/_rels/slideLayout1.xml.rels",
            _simple_rels_xml([("rId1", "slideMaster", "../slideMasters/slideMaster1.xml")]),
        ),
        ("ppt/theme/theme1.xml", _theme_xml()),
    ]
    for i, slide in enumerate(deck.slides):
        parts.append((f"ppt/slides/slide{i + 1}.xml", _slide_xml(slide)))
        parts.append(
            (
                f"ppt/slides/_rels/slide{i + 1}.xml.rels",
                _simple_rels_xml([("rId1", "slideLayout", "../slideLayouts/slideLayout1.xml")]),
            )
        )
    return parts


def write_pptx(deck: RenderedDeck, path) -> None:
    """Write the deck to `path` as a .pptx package.

    Determinism is pinned on four axes: fixed part order, a frozen ZIP
    timestamp, `create_system = 0`, and stored rather than deflated entries.

    The first three are the obvious ones. Without the `create_system` pin,
    `zipfile` records the host OS in each entry, so the same deck built on Linux
    and macOS differs byte-for-byte while a build-twice test passes on both.

    The fourth is the subtle one. DEFLATE output is not specified byte-for-byte:
    zlib, zlib-ng and vendor-tuned builds all emit different compressed streams
    for identical input at the same level. Compressing would therefore make the
    file depend on which zlib CPython happens to be linked against -- a host
    dependency that no same-machine test could ever reveal. Storing the parts
    uncompressed removes the compressor from the output entirely. A deck of
    shapes and text is tens of kilobytes either way, so the cost is nothing next
    to a guarantee that actually holds.
    """
    parts = build_parts(deck)
    with zipfile.ZipFile(path, "w", compression=zipfile.ZIP_STORED) as archive:
        for name, content in parts:
            info = zipfile.ZipInfo(name, date_time=FROZEN_ZIP_DATE)
            info.compress_type = zipfile.ZIP_STORED
            info.create_system = 0  # 0 = MS-DOS, constant across platforms
            info.external_attr = 0o600 << 16
            archive.writestr(info, content.encode("utf-8"))
